package 과제.팀과제2;

public class MemberInfo {

    int no;
    String name;
    String id;
    String pw;
    String num;
    String birth;


    public MemberInfo(int no, String name, String id, String pw, String num, String birth) {
        this.no = no;
        this.name = name;
        this.id = id;
        this.pw = pw;
        this.num = num;
        this.birth = birth;
    }


}
